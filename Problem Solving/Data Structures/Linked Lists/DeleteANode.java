    static SinglyLinkedListNode deleteNode(SinglyLinkedListNode head, int position) {
            if (head == null)
        return null;
        
        if(position==0){
            if(head.next==null) return null;
            head=head.next;
            return head;
        }
        int pos=0;
        for(SinglyLinkedListNode temp=head;temp.next!=null;temp=temp.next){
            if(pos==position-1 ){
                if(temp.next.next!=null)
                    temp.next=temp.next.next;
                else temp.next=null;
            }
            pos++;
        }
        return head;
    }

